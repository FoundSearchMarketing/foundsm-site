#!/usr/bin/env node

import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

import { importModernBlogPosts } from './lib/import-modern-posts.mjs';

const projectRoot = process.cwd();
const sourceDir = process.argv[2]
  ? path.resolve(projectRoot, process.argv[2])
  : '/Users/theoden/Documents/test/blogposts-modern';
const outFile = process.argv[3]
  ? path.resolve(projectRoot, process.argv[3])
  : path.join(projectRoot, 'src', 'data', 'blogPosts.generated.ts');

const posts = await importModernBlogPosts(sourceDir);

const source = `// Generated by scripts/blog-posts/generate-blog-post-data.mjs
// Source: ${sourceDir}

import type { BlogPost } from '../lib/blogPosts';

export const generatedBlogPosts = ${JSON.stringify(posts, null, 2)} satisfies BlogPost[];
`;

await mkdir(path.dirname(outFile), { recursive: true });
await writeFile(outFile, source, 'utf-8');

console.log(`Generated ${path.relative(projectRoot, outFile)} (${posts.length} posts)`);
